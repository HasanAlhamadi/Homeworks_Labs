public class Person {
    protected String name;
    protected int birthyear;

    public Person(String name, int birthyear) {
        this.name = name;
        this.birthyear = birthyear;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setBirthyear(int birthyear) {
        this.birthyear = birthyear;
    }


}
